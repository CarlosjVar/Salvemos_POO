    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         // TODO add your handling code here:
         if(!jTextField1.getText().isEmpty()&&!jTextField2.getText().isEmpty()&&!jTextField3.getText().isEmpty()&&!jTextField4.getText().isEmpty())
            try {
                
                int duracion=Integer.parseInt(jTextField3.getText());
                Programa progra=new Programa(jTextField1.getText(),jTextField2.getText(),duracion,jTextField4.getText());
                this.dispose();
            }
            catch(NumberFormatException nfe){
              JOptionPane.showMessageDialog(null, "Ingrese un numero entero!");     
            }
         else if(jTextField1.getText().isEmpty()&&jTextField2.getText().isEmpty()&&jTextField3.getText().isEmpty()&&jTextField4.getText().isEmpty())
         {
            JOptionPane.showMessageDialog(null, "Rellene todos los espacios por favor");
         }
         else if(jTextField1.getText().isEmpty())
         {   
            JOptionPane.showMessageDialog(null, "Digite un nombre");
         }
         else if(jTextField2.getText().isEmpty())
         {   
            JOptionPane.showMessageDialog(null, "Digite un horario");
         }
         else if(jTextField3.getText().isEmpty())
         {   
            JOptionPane.showMessageDialog(null, "Digite una duracion");
         }
         else if(jTextField4.getText().isEmpty())
         {   
            JOptionPane.showMessageDialog(null, "Digite un Genero");
         }
    }//GEN-LAST:event_jButton1ActionPerformed


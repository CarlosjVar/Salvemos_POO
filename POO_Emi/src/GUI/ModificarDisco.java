    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int returnVal = img.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = img.getSelectedFile();
            try {
                // What to do with the file, e.g. display it in a TextArea
                String path=file.getPath();
                jTextField5.setText(path);
            }  catch (Exception e) {
                System.out.println("problem accessing file"+file.getAbsolutePath());
            }
        } else {
            System.out.println("File access cancelled by user.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().isEmpty()&&!jTextField2.getText().isEmpty()&&!jTextField3.getText().isEmpty()&&!jTextField4.getText().isEmpty()&&!jTextField6.getText().isEmpty())
        try {
            int edicion=Integer.parseInt(jTextField3.getText());
            Disco disco=MainWindow.getDisk(ComboDisk.getSelectedItem().toString());
            if(jTextField5.getText().isEmpty())
            {
                disco.setNombre(jTextField1.getText());
                disco.setCantante(jTextField2.getText());
                disco.setAnoEdicion(edicion);
                disco.setGenero(jTextField4.getText());
                disco.setDireccion(jTextField6.getText());
            }
            else
            {
                disco.setNombre(jTextField1.getText());
                disco.setCantante(jTextField2.getText());
                disco.setAnoEdicion(edicion);
                disco.setGenero(jTextField4.getText());
                disco.setImagen(jTextField5.getText());
                disco.setDireccion(jTextField6.getText());
            }
            MainWindow.addDisk(disco);
            this.dispose();
        }

        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Ingrese un numero!");
        }
        else if(jTextField1.getText().isEmpty()&&jTextField2.getText().isEmpty()&&jTextField3.getText().isEmpty()&&jTextField4.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Rellene todos los espacios por favor");
        }
        else if(jTextField1.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Digite un nombre");
        }
        else if(jTextField2.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Digite un cantante");
        }
        else if(jTextField3.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Digite una aÃ±o de edicion");
        }
        else if(jTextField4.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Digite un Genero");
        }
        else if(jTextField6.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Digite un Descripcion de la Ubicacion");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


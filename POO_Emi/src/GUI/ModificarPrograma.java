    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jTextField2.getText().isEmpty()&&!jTextField3.getText().isEmpty()&&!jTextField4.getText().isEmpty())
        try {
            Programa loc=new Programa();
            int duracion=Integer.parseInt(jTextField3.getText());
            Programa progra=MainWindow.getProg((String)SelecProg.getSelectedItem());
            progra.setDuracion(duracion);
            progra.setGenero(jTextField4.getText());
            progra.setHorario(jTextField2.getText());
            progra.setNombre(jTextField5.getText());
            Locutor locu=MainWindow.getLocu((String)locutores.getSelectedItem());
            progra.setLocutor(locu);
            JOptionPane.showMessageDialog(null, "Se ha modificado con exito");
            this.dispose();

        }

        catch(NumberFormatException nfe){
            JOptionPane.showMessageDialog(null, "Ingrese un numero entero!");
        }
        else if(jTextField5.getText().isEmpty()&&jTextField2.getText().isEmpty()&&jTextField3.getText().isEmpty()&&jTextField4.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Rellene todos los espacios por favor");
        }
        else if(jTextField5.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Digite un nombre");
        }
        else if(jTextField2.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Digite un horario");
        }
        else if(jTextField3.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Digite una duracion");
        }
        else if(jTextField4.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Digite un Genero");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


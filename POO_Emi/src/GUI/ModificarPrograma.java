    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        Radioemisora radio=MainWindow.getEmisora();
        ArrayList <Programa> listapogo= radio.getProg();
        Programa pogo=new Programa();
        for(int i = 0; i < listapogo.size(); i++)
        {
            if(listapogo.get(i).getNombre()==SelecProg.getSelectedItem())
            {
                pogo=listapogo.get(i);
            }
        }
        jTextField5.setText(pogo.getNombre());
        jTextField2.setText(pogo.getHorario());
        jTextField3.setText(Integer.toString(pogo.getDuracion()));
        jTextField4.setText(pogo.getGenero());
    }//GEN-LAST:event_jButton3ActionPerformed


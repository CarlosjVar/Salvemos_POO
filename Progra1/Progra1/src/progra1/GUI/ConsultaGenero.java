    private void buscarGenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarGenActionPerformed
        
        RadioEmisora r = Principal.getEmisora();
        ArrayList<Disco> listDis = r.getDiscos();
        ArrayList<Sencillo> listSen = r.getSencillos();
        
        String genero = tbConsultaGenero.getText();
        
        DefaultTableModel model = (DefaultTableModel) tableGenero.getModel();
        model.setRowCount(0);
  
        for(Disco d : listDis){
  
            
            ArrayList<Cancion> listC = d.getCanciones();
            for(Cancion c : listC){
   
                if(c.getGenero().equals(genero)){
   
                    model.addRow(new Object[]{c.getNombre(), c.getCantante(), d.getNombre()});
                }
            }
        }
        
        for(Sencillo s : listSen){
            if(s.getGenero().equals(genero)){
                model.addRow(new Object[]{s.getNombre(), s.getCantante(), s.getAlbum()});
            }
        }
    }//GEN-LAST:event_buscarGenActionPerformed

